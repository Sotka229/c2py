/* src/codegen.h — генерация кода на языке Python.
 *
 * Генератор работает по размеченному дереву, а не по потоку лексем,
 * поэтому видит каждую конструкцию целиком и знает типы. Это позволяет
 * принимать решения, недоступные при построчном выводе:
 *   - выбирать между "/" и "//" по типу операндов;
 *   - сводить for к range() только когда это безопасно;
 *   - расставлять скобки по приоритетам операций Python,
 *     которые для побитовых операций отличаются от C.
 */
#ifndef C2PY_CODEGEN_H
#define C2PY_CODEGEN_H

#include "ast.h"
#include "symtab.h"

/* Возвращает текст программы на Python (нужно освободить через free).
 * Дерево должно быть предварительно проверено sema_check. */
char *codegen_program(Program *prog, SymTab *tab);

#endif /* C2PY_CODEGEN_H */

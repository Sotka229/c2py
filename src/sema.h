/* src/sema.h — семантический анализ.
 *
 * Этап, которого не было в исходном варианте транслятора. Он связывает
 * каждое имя с объявлением, вычисляет тип каждого выражения и проверяет
 * то, что синтаксис проверить не может.
 *
 * Без него невозможно правильно выбрать между "/" и "//", определить
 * начальное значение переменной или отличить массив от скаляра.
 */
#ifndef C2PY_SEMA_H
#define C2PY_SEMA_H

#include "ast.h"
#include "symtab.h"

/* Проверяет программу и размечает дерево: расставляет Expr.type,
 * связывает Expr.u.ident.sym и Declarator.sym с символами.
 * Возвращает 1, если ошибок нет. Подробности — через модуль diag. */
int sema_check(Program *prog, SymTab *tab);

/* Размер типа в байтах, как его видит sizeof. */
int sema_type_size(const Type *t);

/* Вычисляет константное целое выражение. Возвращает 1 при успехе. */
int sema_const_int(const Expr *e, long long *out);

#endif /* C2PY_SEMA_H */

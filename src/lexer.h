/* src/lexer.h — интерфейс лексического анализатора.
 *
 * Лексер намеренно не выводит ничего в результат трансляции: он только
 * распознаёт лексемы и складывает комментарии в отдельную таблицу.
 */
#ifndef C2PY_LEXER_H
#define C2PY_LEXER_H

#include <stdio.h>

/* Начинает разбор текста, уже находящегося в памяти:
 * так читается программа со стандартного ввода. */
void lexer_start_string(const char *src);

/* Начинает разбор открытого файла. */
void lexer_start_file(FILE *fp);

/* Освобождает буфер и сбрасывает состояние. */
void lexer_finish(void);

int yylex(void);

#endif /* C2PY_LEXER_H */

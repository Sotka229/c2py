/* src/escape.h — работа с escape-последовательностями.
 *
 * Разделены две задачи, которые в исходном варианте были смешаны:
 *   - раскодировать литерал C в набор байтов (делает лексер);
 *   - закодировать набор байтов в литерал Python (делает генератор).
 * Между ними в дереве хранится уже "чистое" значение, поэтому
 * escape-последовательности не приходится переносить вслепую.
 */
#ifndef C2PY_ESCAPE_H
#define C2PY_ESCAPE_H

#include "srcpos.h"

/* Раскодирует содержимое литерала (без кавычек) в набор байтов.
 * Возвращает буфер, выделенный malloc, завершённый нулём;
 * длина (без завершающего нуля) записывается в *out_len. */
char *escape_decode(const char *src, int len, int *out_len, SrcPos pos);

/* Кодирует байты в строковый литерал Python вместе с кавычками. */
char *escape_to_python_string(const char *data, int len);

#endif /* C2PY_ESCAPE_H */
